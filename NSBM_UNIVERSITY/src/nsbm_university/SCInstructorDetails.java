package nsbm_university;

/**
 *
 * @author USER
 */
public class SCInstructorDetails {
    private int Instructer_Id;
    private String Name;
    private String Sub_Code;
    private String Email;
    private String Tel_No;

    /**
     * @return the Instructer_Id
     */
    public int getInstructer_Id() {
        return Instructer_Id;
    }

    /**
     * @param Instructer_Id the Instructer_Id to set
     */
    public void setInstructer_Id(int Instructer_Id) {
        this.Instructer_Id = Instructer_Id;
    }

    /**
     * @return the Name
     */
    public String getName() {
        return Name;
    }

    /**
     * @param Name the Name to set
     */
    public void setName(String Name) {
        this.Name = Name;
    }

    /**
     * @return the Sub_Code
     */
    public String getSub_Code() {
        return Sub_Code;
    }

    /**
     * @param Sub_Code the Sub_Code to set
     */
    public void setSub_Code(String Sub_Code) {
        this.Sub_Code = Sub_Code;
    }

    /**
     * @return the Email
     */
    public String getEmail() {
        return Email;
    }

    /**
     * @param Email the Email to set
     */
    public void setEmail(String Email) {
        this.Email = Email;
    }

    /**
     * @return the Tel_No
     */
    public String getTel_No() {
        return Tel_No;
    }

    /**
     * @param Tel_No the Tel_No to set
     */
    public void setTel_No(String Tel_No) {
        this.Tel_No = Tel_No;
    }
}
